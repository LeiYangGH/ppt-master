#!/usr/bin/env python3
"""
SVG XML Auto-Repair Tool

Repairs malformed SVG/XML files generated by LLMs using the sloppy-xml library.
Common issues fixed:
  - HTML named entities (&nbsp; &mdash; &copy; etc.)
  - Unclosed / mismatched tags
  - Bare ampersands and angle brackets in text
  - Malformed attributes (missing quotes, mixed quotes)
  - Broken comments and CDATA sections
  - Duplicate attributes

用法：
    python scripts/svg_repair.py

Usage (as library):
    from svg_repair import repair_svg_file, repair_svg_text
    repaired, report = repair_svg_text(svg_string)
    count = repair_svg_file(Path("slide_01.svg"))
"""

import sys
import xml.etree.ElementTree as ET
from dataclasses import dataclass, field
from pathlib import Path
from typing import Optional

try:
    import sloppy_xml
    from sloppy_xml import RecoveryStrategy, ParseError
    HAS_SLOPPY_XML = True
except ImportError:
    HAS_SLOPPY_XML = False


# ── Data structures ──────────────────────────────────────────────────

@dataclass
class RepairReport:
    """Result of a single SVG repair attempt."""
    file: Optional[str] = None
    was_valid: bool = True
    is_valid: bool = True
    repaired: bool = False
    recovery_actions: list[str] = field(default_factory=list)
    error: Optional[str] = None

    @property
    def summary_line(self) -> str:
        if self.was_valid:
            return "already valid"
        if self.repaired:
            actions = "; ".join(self.recovery_actions[:3])
            extra = f" (+{len(self.recovery_actions)-3} more)" if len(self.recovery_actions) > 3 else ""
            return f"repaired ({actions}{extra})"
        return f"repair failed: {self.error or 'unknown'}"


# ── Core repair logic ────────────────────────────────────────────────

def _is_well_formed(text: str) -> bool:
    """Return True if *text* parses as well-formed XML."""
    try:
        ET.fromstring(text)
        return True
    except ET.ParseError:
        return False


def repair_svg_text(svg_text: str) -> tuple[str, RepairReport]:
    """Attempt to repair *svg_text* and return (repaired_text, report).

    If the input is already well-formed XML, it is returned unchanged.
    When sloppy-xml is not installed, the original text is returned with
    an error note in the report.
    """
    report = RepairReport()

    # Fast path: already valid
    if _is_well_formed(svg_text):
        report.was_valid = True
        report.is_valid = True
        return svg_text, report

    report.was_valid = False

    if not HAS_SLOPPY_XML:
        report.is_valid = False
        report.error = "sloppy-xml not installed (pip install sloppy-xml)"
        return svg_text, report

    try:
        # Collect recovery diagnostics via stream_parse
        events = list(sloppy_xml.stream_parse(
            svg_text,
            recover=True,
            emit_errors=True,
            recovery_strategy=RecoveryStrategy.AGGRESSIVE,
            repair_attributes=True,
            smart_quotes=True,
            auto_close_tags=True,
            # Keep entities as-is for the stream report; tree_parse resolves them.
            resolve_entities=False,
        ))
        for ev in events:
            if isinstance(ev, ParseError):
                report.recovery_actions.append(
                    f"[{ev.error_type}] {ev.message}"
                )

        # Build a well-formed ElementTree
        root = sloppy_xml.tree_parse(
            svg_text,
            recover=True,
            recovery_strategy=RecoveryStrategy.AGGRESSIVE,
            repair_attributes=True,
            smart_quotes=True,
            auto_close_tags=True,
            # Resolve HTML entities → Unicode so ET serialization stays valid.
            resolve_entities=True,
        )

        # Serialize back to string
        repaired = ET.tostring(root, encoding="unicode")

        # Re-register default SVG namespace for cleaner output
        if 'xmlns="http://www.w3.org/2000/svg"' in svg_text:
            # ET may have prefixed tags with {http://...}; register ns
            ET.register_namespace("", "http://www.w3.org/2000/svg")
            ET.register_namespace("xlink", "http://www.w3.org/1999/xlink")
            repaired = ET.tostring(root, encoding="unicode")

        # Validate the result
        if _is_well_formed(repaired):
            report.is_valid = True
            report.repaired = True
        else:
            report.is_valid = False
            report.error = "sloppy-xml output still not well-formed"
            return svg_text, report

        return repaired, report

    except Exception as exc:
        report.is_valid = False
        report.error = str(exc)
        return svg_text, report


def repair_svg_file(
    svg_path: Path,
    *,
    dry_run: bool = False,
    verbose: bool = False,
) -> RepairReport:
    """Repair a single SVG file in-place (unless dry_run).

    Returns a RepairReport.
    """
    report = RepairReport(file=svg_path.name)

    try:
        original = svg_path.read_text(encoding="utf-8")
    except Exception as exc:
        report.error = f"read error: {exc}"
        report.is_valid = False
        return report

    repaired, report = repair_svg_text(original)
    report.file = svg_path.name

    if report.repaired and not dry_run:
        svg_path.write_text(repaired, encoding="utf-8")
        if verbose:
            _safe_print(f"  [REPAIRED] {svg_path.name}: {report.summary_line}")
    elif report.was_valid and verbose:
        _safe_print(f"  [OK] {svg_path.name}: already valid XML")
    elif not report.is_valid and verbose:
        _safe_print(f"  [ERROR] {svg_path.name}: {report.error}")

    return report


def repair_project_svgs(
    project_dir: Path,
    *,
    subdirs: tuple[str, ...] = ("svg_output",),
    dry_run: bool = False,
    quiet: bool = False,
) -> tuple[int, int, int]:
    """Repair all SVG files in a project directory.

    Scans *subdirs* (default: svg_output/) for .svg files and repairs
    any that are not well-formed XML.

    Returns (total, repaired, failed).
    """
    total = repaired = failed = 0

    for subdir_name in subdirs:
        svg_dir = project_dir / subdir_name
        if not svg_dir.is_dir():
            continue
        for svg_file in sorted(svg_dir.glob("*.svg")):
            total += 1
            report = repair_svg_file(svg_file, dry_run=dry_run, verbose=not quiet)
            if report.repaired:
                repaired += 1
            elif not report.is_valid and not report.was_valid:
                failed += 1

    return total, repaired, failed


# ── CLI ──────────────────────────────────────────────────────────────

def _safe_print(text: str) -> None:
    try:
        print(text)
    except UnicodeEncodeError:
        print(text.encode("ascii", errors="replace").decode("ascii"))


def main() -> None:
    from scripts.pathutil import SVG_OUTPUT_DIR
    
    if not HAS_SLOPPY_XML:
        _safe_print("[ERROR] sloppy-xml is not installed. Run: pip install sloppy-xml")
        sys.exit(1)

    target = SVG_OUTPUT_DIR
    dry_run = False
    quiet = False

    if not target.exists():
        _safe_print(f"[ERROR] SVG output directory not found: {target}")
        sys.exit(1)

    # Project mode
    if not quiet:
        _safe_print(f"[DIR] Scanning {target.name}/ for malformed SVGs...")
        print()

    total, repaired, failed = repair_project_svgs(
        target, dry_run=dry_run, quiet=quiet,
    )

    if not quiet:
        print()
        _safe_print(f"[SUMMARY] {total} file(s) scanned, "
                    f"{repaired} repaired, {failed} failed")
        if repaired and not dry_run:
            _safe_print("[OK] Repaired files written back.")

    if failed:
        sys.exit(1)


if __name__ == "__main__":
    main()
